package com.budko.translatorproject.entities;

/**
 * @author dBudko
 */
public class PolizEntity {
    private String inputLexeme;
    private String stack;
    private String poliz;

    public String getInputLexeme() {
        return inputLexeme;
    }

    public void setInputLexeme(String inputLexeme) {
        this.inputLexeme = inputLexeme;
    }

    public String getStack() {
        return stack;
    }

    public void setStack(String stack) {
        this.stack = stack;
    }

    public String getPoliz() {
        return poliz;
    }

    public void setPoliz(String poliz) {
        this.poliz = poliz;
    }
}
